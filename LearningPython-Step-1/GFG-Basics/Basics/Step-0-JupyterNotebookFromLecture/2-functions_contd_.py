# Ques 5 : Calculate the factorial of a number using funtions

def fact(num):
    if num < 0:
        return "Factorial is not defined."
    elif num  ==0 or num == 1:
        return 1
    else:
        result = 1
        for i in range(2, num + 1):
            result *= i
        return result
number = int(input("Enter a number to find its factorial: "))
print(fact(number))

