a = 8
b = 5
# addition
print ('Addition : ', a + b)
c = True#True treated as 1 and False as 0
print(f'The addition of T+T ={c+True}')
print(f'The addition of T+F ={c+False}')

# subtraction
print ('Subtraction: ', a - b)

# multiplication
print ('Multiplication: ', a * b)

# division
print ('Division: ', a / b)

# floor division
print ('Floor Division: ', a // b)

# modulo
print ('Modulo: ', a % b)

# a to the power b
print ('Power a^b: ', a ** b)

#abs
print(f'The absolute value is :{abs(-35)}')

#round
print(f'The rounded off value is :{round(3.55485452,4)}')#,4 means rounded off to 4 decimal places

#power
print(f'The power of 2^3 is :{pow(2,3)}')#2^3 similar to **
