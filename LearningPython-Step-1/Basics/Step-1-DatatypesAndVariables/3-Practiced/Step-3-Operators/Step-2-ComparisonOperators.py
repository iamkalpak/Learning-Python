x = 5
y = 7
# equal to operator
print(f'the value of x={x} and y={y}')
print('is x equal to y -', x == y)
print('is A equal to a -', 'A'=='a')
print('is True equal to 1 -', True == 1)
print('is False equal to 0 -', False == 0)
print("--------------------------------------")

# not equal to operator
print('is x not equal to y -', x != y)
print('is A not equal to a -', 'A'!='a')
print('is True not equal to 0 -', True != 0)
print('is False not equal to 1 -', False != 1)
print("--------------------------------------")

# greater than operator
print('is x greater than y -', x > y)
print("--------------------------------------")

# less than operator
print('is x less than y -', x < y)
print("--------------------------------------")

# greater than or equal to operator
print('is x greater than or equal to y -', x >= y)
print("--------------------------------------")

# less than or equal to operator
print('is x less than or equal to y -', x <= y)
print("--------------------------------------")
